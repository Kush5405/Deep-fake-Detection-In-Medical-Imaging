import torch
import torch.nn as nn
import torchvision.transforms as transforms
from torchvision.utils import save_image
import os
from tqdm import tqdm # This gives us a cool progress bar!

# ==========================================
# 1. Configuration & Paths
# ==========================================
device = torch.device("cuda:0" if torch.cuda.is_available() else "cpu")
print(f"Factory running on: {device}")

# Where the brain is, and where the fakes are going
model_path = '/content/drive/MyDrive/EPICS/gen_pneumonia.pth'
output_folder = '/content/drive/MyDrive/EPICS/pneumonia_dataset/fake_pneumonia'

# Create the new folder safely
os.makedirs(output_folder, exist_ok=True)

# Factory Settings
num_images_to_generate = 2000
batch_size = 64
nz = 100 # The size of the noise vector

# ==========================================
# 2. Rebuild the Brain (Generator Only!)
# ==========================================
class Generator(nn.Module):
    def __init__(self, nz=100, ngf=64, nc=3):
        super(Generator, self).__init__()
        self.main = nn.Sequential(
            nn.ConvTranspose2d(nz, ngf * 8, 4, 1, 0, bias=False),
            nn.BatchNorm2d(ngf * 8),
            nn.ReLU(True),
            nn.ConvTranspose2d(ngf * 8, ngf * 4, 4, 2, 1, bias=False),
            nn.BatchNorm2d(ngf * 4),
            nn.ReLU(True),
            nn.ConvTranspose2d(ngf * 4, ngf * 2, 4, 2, 1, bias=False),
            nn.BatchNorm2d(ngf * 2),
            nn.ReLU(True),
            nn.ConvTranspose2d(ngf * 2, ngf, 4, 2, 1, bias=False),
            nn.BatchNorm2d(ngf),
            nn.ReLU(True),
            nn.ConvTranspose2d(ngf, nc, 4, 2, 1, bias=False),
            nn.Tanh()
        )
    def forward(self, input):
        return self.main(input)

# Load the graduated AI into memory
netG = Generator().to(device)
netG.load_state_dict(torch.load(model_path, map_location=device))
netG.eval() # CRITICAL: Locks the brain so it stops learning!

# We upscale the 64x64 output to 256x256 so it perfectly matches your Real X-rays
upscale = transforms.Resize((256, 256), antialias=True)

# ==========================================
# 3. The Assembly Line
# ==========================================
print(f"\n🏭 Starting Mass Production of {num_images_to_generate} Fake X-rays...")

with torch.no_grad(): # Turn off math tracking to save RAM and run 10x faster

    images_generated = 0
    pbar = tqdm(total=num_images_to_generate, desc="Forging Images")

    while images_generated < num_images_to_generate:
        # Calculate how many to make in this batch
        current_batch_size = min(batch_size, num_images_to_generate - images_generated)

        # 1. Create pure digital static (noise)
        noise = torch.randn(current_batch_size, nz, 1, 1, device=device)

        # 2. Feed static to the Generator to sculpt the fake X-ray
        fake_images = netG(noise)

        # 3. Upscale them to 256x256
        fake_images = upscale(fake_images)

        # 4. Save them one by one to your Drive
        for i in range(current_batch_size):
            image_tensor = fake_images[i]

            # The GAN outputs colors from -1 to 1. We shift it to 0 to 1 for standard images.
            image_tensor = (image_tensor + 1) / 2.0

            # Save the file
            file_name = os.path.join(output_folder, f"synthetic_pneumonia_{images_generated + i + 1}.jpg")
