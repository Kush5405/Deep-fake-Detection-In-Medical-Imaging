"""GAN Training"""

import torch
import torch.nn as nn
import torch.optim as optim
import torchvision.transforms as transforms
from torch.utils.data import Dataset, DataLoader
from PIL import Image
import os
import glob

# ==========================================
# 1. Configuration & Paths
# ==========================================
device = torch.device("cuda:0" if torch.cuda.is_available() else "cpu")
print(f"Training on: {device}")

# ---> THE MAGIC FIX: Tell the AI where to pick up from! <---
resume_epoch = 450  # Change this to 50, 100, 150, etc., based on your Drive files!
# (Set to 0 if you ever want to start completely from scratch)

data_folder = '/content/drive/MyDrive/EPICS/pneumonia_dataset/pneumonia_256x256'
save_model_path = '/content/drive/MyDrive/EPICS/gen_pneumonia.pth'

batch_size = 32
image_size = 64
nz = 100
num_epochs = 500
lr = 0.0001
beta1 = 0.5

# ==========================================
# 2. Custom Dataset Loader
# ==========================================
class SingleFolderDataset(Dataset):
    def __init__(self, folder_path, transform=None):
        self.image_paths = []
        for ext in ('*.jpg', '*.jpeg', '*.png'):
            self.image_paths.extend(glob.glob(os.path.join(folder_path, ext)))
            self.image_paths.extend(glob.glob(os.path.join(folder_path, ext.upper())))
        self.transform = transform

    def __len__(self):
        return len(self.image_paths)

    def __getitem__(self, idx):
        img_path = self.image_paths[idx]
        image = Image.open(img_path).convert('RGB')
        if self.transform:
            image = self.transform(image)
        return image, 0

transform = transforms.Compose([
    transforms.Resize(image_size),
    transforms.CenterCrop(image_size),
    transforms.ToTensor(),
    transforms.Normalize((0.5, 0.5, 0.5), (0.5, 0.5, 0.5)),
])

dataset = SingleFolderDataset(data_folder, transform=transform)
dataloader = DataLoader(dataset, batch_size=batch_size, shuffle=True, num_workers=2)

# ==========================================
# 3. Neural Network Architectures
# ==========================================
class Generator(nn.Module):
    def __init__(self, nz=100, ngf=64, nc=3):
        super(Generator, self).__init__()
        self.main = nn.Sequential(
            nn.ConvTranspose2d(nz, ngf * 8, 4, 1, 0, bias=False),
            nn.BatchNorm2d(ngf * 8),
            nn.ReLU(True),
            nn.ConvTranspose2d(ngf * 8, ngf * 4, 4, 2, 1, bias=False),
            nn.BatchNorm2d(ngf * 4),
            nn.ReLU(True),
            nn.ConvTranspose2d(ngf * 4, ngf * 2, 4, 2, 1, bias=False),
            nn.BatchNorm2d(ngf * 2),
            nn.ReLU(True),
            nn.ConvTranspose2d(ngf * 2, ngf, 4, 2, 1, bias=False),
            nn.BatchNorm2d(ngf),
            nn.ReLU(True),
            nn.ConvTranspose2d(ngf, nc, 4, 2, 1, bias=False),
            nn.Tanh()
        )
    def forward(self, input):
        return self.main(input)

class Discriminator(nn.Module):
    def __init__(self, nc=3, ndf=64):
        super(Discriminator, self).__init__()
        self.main = nn.Sequential(
            nn.Conv2d(nc, ndf, 4, 2, 1, bias=False),
            nn.LeakyReLU(0.2, inplace=True),
            nn.Conv2d(ndf, ndf * 2, 4, 2, 1, bias=False),
            nn.BatchNorm2d(ndf * 2),
            nn.LeakyReLU(0.2, inplace=True),
            nn.Conv2d(ndf * 2, ndf * 4, 4, 2, 1, bias=False),
            nn.BatchNorm2d(ndf * 4),
            nn.LeakyReLU(0.2, inplace=True),
            nn.Conv2d(ndf * 4, ndf * 8, 4, 2, 1, bias=False),
            nn.BatchNorm2d(ndf * 8),
            nn.LeakyReLU(0.2, inplace=True),
            nn.Conv2d(ndf * 8, 1, 4, 1, 0, bias=False),
            nn.Sigmoid()
        )
    def forward(self, input):
        return self.main(input)

netG = Generator().to(device)
netD = Discriminator().to(device)

# ==========================================
# 3.5 THE RESUME MODULE (Curing the Amnesia)
# ==========================================
if resume_epoch > 0:
    checkpoint_path = f'/content/drive/MyDrive/EPICS/gen_pneumonia_epoch_{resume_epoch}.pth'
    if os.path.exists(checkpoint_path):
        print(f"\n🔄 WAKING UP AI: Loading Brain from Epoch {resume_epoch}...")
        netG.load_state_dict(torch.load(checkpoint_path, map_location=device))
        print(f"✅ Success! Resuming training from Epoch {resume_epoch}.")
    else:
        print(f"\n⚠️ ERROR: Could not find {checkpoint_path}. Did you type the number correctly?")
        print("Starting from scratch instead.")
        resume_epoch = 0
else:
    print("\n🌱 Starting training completely from scratch (Epoch 0).")

# ==========================================
# 4. Training Loop
# ==========================================
criterion = nn.BCELoss()
optimizerD = optim.Adam(netD.parameters(), lr=lr, betas=(beta1, 0.999))
optimizerG = optim.Adam(netG.parameters(), lr=lr, betas=(beta1, 0.999))

print("\n🚀 Training Initiated...")

# NOTE: The loop now starts at your resume_epoch instead of 0!
for epoch in range(resume_epoch, num_epochs):
    for i, data in enumerate(dataloader, 0):
        # Train Discriminator
        netD.zero_grad()
        real_cpu = data[0].to(device)
        b_size = real_cpu.size(0)

        label = torch.full((b_size,), 0.9, dtype=torch.float, device=device)
        output = netD(real_cpu).view(-1)
        errD_real = criterion(output, label)
        errD_real.backward()

        noise = torch.randn(b_size, nz, 1, 1, device=device)
        fake = netG(noise)
        label.fill_(0.0)
        output = netD(fake.detach()).view(-1)
        errD_fake = criterion(output, label)
        errD_fake.backward()

        errD = errD_real + errD_fake
        optimizerD.step()

        # Train Generator
        netG.zero_grad()
        label.fill_(0.9)
        output = netD(fake).view(-1)
        errG = criterion(output, label)
        errG.backward()
        optimizerG.step()

    # Save and Print Progress
    if (epoch + 1) % 50 == 0:
        print(f'Epoch [{epoch+1}/{num_epochs}] | Loss_D: {errD.item():.4f} | Loss_G: {errG.item():.4f}')
        checkpoint_name = f'/content/drive/MyDrive/EPICS/gen_pneumonia_epoch_{epoch+1}.pth'
        torch.save(netG.state_dict(), checkpoint_name)
        print(f"💾 Backup saved: {checkpoint_name}")

torch.save(netG.state_dict(), save_model_path)
print(f"\n✅ Success! Final GAN weights securely saved to: {save_model_path}")

