# 2. The Resizing Engine
def batch_resize(input_folder, output_folder, class_name):
    print(f"\n🚀 Starting batch resize for {class_name}...")

    if not os.path.exists(input_folder):
        print(f"⚠️ Error: Could not find {input_folder}")
        return

    processed_count = 0
    valid_extensions = ('.jpg', '.jpeg', '.png')

    for filename in os.listdir(input_folder):
        if filename.lower().endswith(valid_extensions):
            img_path = os.path.join(input_folder, filename)
            out_path = os.path.join(output_folder, filename)

            # Skip if already processed (this is great if Colab ever crashes and you need to restart!)
            if os.path.exists(out_path):
                processed_count += 1
                continue

            try:
                with Image.open(img_path) as img:
                    # Convert to standard RGB to prevent channel errors in PyTorch
                    img = img.convert('RGB')

                    # Resize to exactly 256x256 using the highest quality filter (LANCZOS)
                    resized_img = img.resize((256, 256), Image.Resampling.LANCZOS)

                    # Save the image
                    resized_img.save(out_path)

                    processed_count += 1

                    # Print progress every 500 images so you know it hasn't frozen
                    if processed_count % 500 == 0:
                        print(f"Resized {processed_count} {class_name} images...")

            except Exception as e:
                print(f"⚠️ Could not process {filename}: {e}")

    print(f"✅ Success! Resized {processed_count} {class_name} images to 256x256.")

# 3. Execute the process for both classes
batch_resize(input_pn, output_pn, "PNEUMONIA")
batch_resize(input_nm, output_nm, "NORMAL")

print(f"\n🎉 ALL DONE! Your optimized datasets are ready at:")
print(f"- {output_pn}")
print(f"- {output_nm}")

