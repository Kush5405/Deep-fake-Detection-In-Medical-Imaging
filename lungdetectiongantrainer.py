def copy_files_with_progress(source_dir, dest_dir, class_name):
    # Get all files in the source folder
    files = [f for f in os.listdir(source_dir) if f.endswith(('.jpg', '.jpeg', '.png', '.JPG', '.PNG'))]

    print(f"\n📦 Packing {len(files)} {class_name} images...")
    for file_name in tqdm(files, desc=f"Copying {class_name}"):
        source_file = os.path.join(source_dir, file_name)
        dest_file = os.path.join(dest_dir, file_name)

        # Only copy if it isn't already there (saves time if you have to restart!)
        if not os.path.exists(dest_file):
            shutil.copy(source_file, dest_file)

# Copy the Reals
