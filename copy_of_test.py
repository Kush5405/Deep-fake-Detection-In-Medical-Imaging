# 3. The UPDATED Core Training Function
def train_gan(folder_path, save_name, num_epochs=500):
    device = torch.device("cuda:0" if torch.cuda.is_available() else "cpu")
    print(f"\n{'='*50}\nStarting Training for: {os.path.basename(save_name)}\n{'='*50}")

    # Check if folder has images
    if not os.path.exists(folder_path):
        print(f"ERROR: Folder {folder_path} does not exist. Skipping.")
        return

    # Hyperparameters (UPDATED for stability)
    batch_size = 32
    image_size = 64
    nz = 100
    lr = 0.0001 # LOWERED from 0.0002 to prevent sudden explosions
    beta1 = 0.5

    # Setup Dataset
    dataset = SingleFolderDataset(folder_path, transform=transforms.Compose([
                               transforms.Resize(image_size),
                               transforms.CenterCrop(image_size),
                               transforms.ToTensor(),
                               transforms.Normalize((0.5, 0.5, 0.5), (0.5, 0.5, 0.5)),
                           ]))

    if len(dataset) == 0:
        print(f"ERROR: No images found in {folder_path}. Skipping.")
        return

    dataloader = DataLoader(dataset, batch_size=batch_size, shuffle=True, num_workers=2)

    # Initialize Networks
    netG = Generator().to(device)
    netD = Discriminator().to(device)

    criterion = nn.BCELoss()
    fixed_noise = torch.randn(64, nz, 1, 1, device=device)
    optimizerD = optim.Adam(netD.parameters(), lr=lr, betas=(beta1, 0.999))
    optimizerG = optim.Adam(netG.parameters(), lr=lr, betas=(beta1, 0.999))

    # Training Loop
    for epoch in range(num_epochs):
        for i, data in enumerate(dataloader, 0):
            # Update Discriminator
            netD.zero_grad()
            real_cpu = data[0].to(device)
            b_size = real_cpu.size(0)

            # ---> LABEL SMOOTHING APPLIED HERE <---
            # We use 0.9 instead of 1.0 to prevent the discriminator from getting too confident
            label = torch.full((b_size,), 0.9, dtype=torch.float, device=device)

            output = netD(real_cpu).view(-1)
            errD_real = criterion(output, label)
            errD_real.backward()

            noise = torch.randn(b_size, nz, 1, 1, device=device)
            fake = netG(noise)
            label.fill_(0.0) # Fakes stay at 0.0

            output = netD(fake.detach()).view(-1)
            errD_fake = criterion(output, label)
            errD_fake.backward()

            errD = errD_real + errD_fake
            optimizerD.step()

            # Update Generator
            netG.zero_grad()
            label.fill_(0.9) # Generator wants to hit 0.9 now

            output = netD(fake).view(-1)
            errG = criterion(output, label)
            errG.backward()
            optimizerG.step()

        # Print update every 100 epochs
        if (epoch + 1) % 100 == 0:
            print(f'Epoch [{epoch+1}/{num_epochs}] | Loss_D: {errD.item():.4f} | Loss_G: {errG.item():.4f}')

    # Save the model
    torch.save(netG.state_dict(), save_name)
    print(f"Success! Model saved to {save_name}")

    del netG, netD, optimizerG, optimizerD, real_cpu, fake
    torch.cuda.empty_cache()

